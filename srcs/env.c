#include "minishell.h"

int	env(int argc, char **argv, char **envp)
{
	int	i;

	i = -1;
	ft_putendl(argv[0]);
	if (argc == 1)
	{
		while (envp[++i])
			ft_putendl(envp[i]);
	}
	return (1);
}
